// Make the generated `Res` class public so the desktopApp module can reach
// `Res.string.*` / `Res.array.*` accessors.
compose.resources {
    publicResClass = true
}

// Generates `dev/hackathon/linkopener/BuildVersion.kt` so that
// AppInfoRepositoryImpl (Settings sidebar) can read the same version that
// desktopApp/build.gradle.kts uses for DMG metadata. Single source of truth:
// `linkopener.version` in root gradle.properties.
//
// Resolve Providers at configuration time and capture only the flat
// String / File values in the doLast lambda — Gradle's configuration cache
// can't serialize Provider references that close over the script object.
val generateBuildVersion = tasks.register("generateBuildVersion") {
    val versionString: String = providers.gradleProperty("linkopener.version").get()
    val outputFile = layout.buildDirectory
        .file("generated/version/dev/hackathon/linkopener/BuildVersion.kt")
        .get().asFile
    inputs.property("version", versionString)
    outputs.file(outputFile)
    doLast {
        outputFile.parentFile.mkdirs()
        outputFile.writeText(
            """
            |// Generated by :shared:generateBuildVersion. Do not edit by hand —
            |// bump `linkopener.version` in root gradle.properties instead.
            |package dev.hackathon.linkopener
            |
            |internal const val APP_VERSION: String = "$versionString"
            |""".trimMargin()
        )
    }
}
val generatedVersionDir = layout.buildDirectory.dir("generated/version")

kotlin {
    jvm {
