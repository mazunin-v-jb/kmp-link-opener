        }
    }
}

// Make every Kotlin compile pick up the freshly-generated BuildVersion.kt.
tasks.withType<org.jetbrains.kotlin.gradle.tasks.KotlinCompilationTask<*>>().configureEach {
    dependsOn(generateBuildVersion)
}

